			<h5 class="barlaw">Composição Cyberpunk</h5>
			<p>Cyberpunk (de Cyber(netic) + punk) ou Cibernarquismo é um subgênero de ficção
			científica, conhecido por seu enfoque de “Alta tecnologia e baixo nível de vida” (“High
			tech, Low life”) e toma seu nome da combinação de cibernética e punk. Mescla ciência
			avançada, como as tecnologias de informação e a cibernética junto com algum grau de
			desintegração ou mudança radical na ordem social. De acordo com Lawrence Person:
			“Os personagens do cyberpunk clássico são seres marginalizados, distanciados, solitários, que vivem à margem da sociedade, geralmente em futuros distópicos onde a vida
			diária é impactada pela rápida mudança tecnológica, uma atmosfera de informação
			computadorizada ambígua e a modificação invasiva do corpo humano.”</p>
			<p>Segundo William Gibson, em seu livro Neuromancer, o indivíduo cyberpunk é uma espécie de “pichador virtual” que se utiliza de seu conhecimento acima da média dos usuários para realizar protestos contra a sistemática vigente das grandes corporações, sob a
			forma de vandalismo com cunho depreciativo, a fim de infligir-lhes prejuízos sem, contudo, auferir qualquer ganho pessoal com tais atos.</p>
			<p>O termo “Cyberpunk” também significa uma subcultura que é focada na Cybercultura
			e se destaca pela preferencia por música psicodélica e de gêneros de fusão entre punk
			rock e música eletrônica e por adereços de moda futuristas.</p>
			<p>O estilo ciberpunk descreve o lado niilista e underground da sociedade digital que
			começou a se desenvolver nas últimas duas décadas do século XX. Um mundo ciberpunk
			distópico é chamado de antítese das visões utópicas de mundos de ficção científica de 
			meados do século XX, como tipificadas pelo mundo de Jornada nas Estrelas (Star Trek),
			embora incorporando algumas dessas utopias, principalmente na questão da separação
			entre corpo e mente, muito discutida na filosofia cartesiana.</p>
			<p>Na literatura ciberpunk, muito da ação se ambienta virtualmente, no ciberespaço - a
			fronteira evidente entre o real e o virtual fica embaçada. Uma característica típica (ainda
			que não universal) desse gênero é uma ligação direta entre o cérebro humano e sistemas
			de computador.</p>
			<p>O mundo ciberpunk é um lugar sinistro, sombrio, com computadores ligados em rede
			que dominam todos os aspectos da vida cotidiana. Empresas multinacionais gigantes
			substituíram o Estado como centros de poder. A batalha do excluído alienado contra
			um sistema totalitário é um tema comum na ficção científica; entretanto, na FC convencional tais sistemas tendem a ser estéreis, ordenados, e controlados pelo Estado. Em
			contraste a isso, no ciberpunk, mostram-se as entranhas da corporatocracia, e a batalha
			sisífica entre seu poder por renegados desiludidos.</p>
			<p>As histórias ciberpunk são vistas como representações ficcionais do presente a partir de
			uma extrapolação e especulação das tecnologias de comunicação, como por exemplo,
			a internet.</p>
			<p>O argumento da escrita cyberpunk se centra em um conflito entre hackers, inteligências artificiais, e megacorporações, tendentes a serem postos dentro da Terra num futuro próximo, em oposição do futuro distante panorama de encontros galácticos em
			romances como a Fundação de Isaac Asimov ou Dune de Frank Herbert. As visões deste
			futuro tendem a ser distopias pós-industriais, mas estão normalmente marcadas por um
			fomento cultural extraordinário e o uso de tecnologias em âmbitos nunca antecipados
			por seus criadores (“A rua encontra suas próprias aplicações pras coisas”). A atmosfera
			do gênero em sua maioria faz eco no cine negro e se utiliza pouco neste gênero técnicas
			de romances policiais. Entre os primeiros expoentes do gênero cyberpunk se encontran
			William Gibson, Bruce Sterling, Pat Cadigan, Rudy Rucker e John Shirley. As influências
			do Cyberpunk se estenderam por outros gêneros literários, e são visíveis, por exemplo,
			em traços da obra de Stephen King, como no livro O Concorrente, onde o mundo é um
			cenário Cyberpunk próximo temporalmente. O termo Cyberpunk se cunhou nos anos 80
			e continua sendo atual.</p>
			<p>Diferente da ficção científica da New Wave, que importou as técnicas e as preocupações
			estilísticas que já existiam na literatura e na cultura, o cyberpunk se originou na ficção
			científica primeiro, antes de incrementar a tendência dominante de sua exposição. No
			começo e meio dos anos 80, o cyberpunk se converteu num tema de moda nos círculos acadêmicos, onde começou a ser objeto de investigação do pós-modernismo. Neste
			mesmo período, o gênero ingressou a Hollywood e se converteu em um dos estilos da
			ficção científica do segmento do cinema. Muitos filmes influentes tais como Blade Runner e a trilogia de Matrix se podem ver como consequências proeminentes dos estilos e
			dos temas do gênero. Os jogos de computador, os jogos de tabuleiro e os jogos de RPG,
			tais como Shadowrun, ou o apropriadamente nomeado Cyberpunk 2020, oferecem a
			miúdo roteiros que estão fortemente influenciados pelos filmes e a literatura cyberpunk.
			Iniciando os anos 90, algumas tendências da moda e a música foram etiquetadas como
			tal.</p>
			<p>Enquanto que uma grande variedade de escritores começou a trabalhar com conceitos
			do cyberpunk, novos sub-gêneros emergiram, que se centravam na tecnologia e seus
			efeitos sociais de uma maneira diferente. Os exemplos incluem o steampunk, iniciado
			por Tim Powers, Kevin Wayne Jeter e James Blaylock, e o biopunk (ou alternati vamente
			ribofunk), no qual Paul Di Filippo é proeminente. Adicionalmente algumas pessoas consideram trabalhos tais como A era do Diamante de Neal Stephenson como o início da
			catégoria pós-cyberpunk.</p>

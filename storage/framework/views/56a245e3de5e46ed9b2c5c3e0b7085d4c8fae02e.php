				o ambiente ao redor do seu objeto de gravação.
			</p>

			<h5>Câmeras com atitude</h5>

			<p>
				Além dos planos e movimentos, a câmera ainda pode transmitir ideias através de sua atitude
				em cena, recebendo assim alguns nomes característicos como, câmera baixa, câmera alta,
				câmera subjetiva e câmera nervosa.
			</p>

			<h5>Câmera baixa/câmera nervosa</h5>

			<p>
				A altura em que posicionamos a câmera no momento da filmagem influencia muito a visão do
				espectador - uma câmera posicionada acima da cabeça do personagem passa a nítida sensação 
				de inferioridade, enquanto a câmera baixa passa a sensação de superioridade. Em campanhas 
				eleitorais é muito comum a utilização da camera baixa para a produção da imagem de
				superioridade do candidato. A altura da câmera também pode ser utilizada em um filme para
				desenhar as linhas de força de cada personagem.
			</p>

			<h5>Câmera subjetiva</h5>

			<p>
				Em cena, a câmera pode tomar duas atitudes, a do espectador assistindo o desenrolar do fato
				de forma neutra e passiva (camera objetiva), ou a atitude do personagem, interagindo na cena
				ao similar sua visão (câmera subjetiva). O recurso da câmera subjetiva é amplamente utilizado
				na construção narrativa de muitos filmes.
			</p>


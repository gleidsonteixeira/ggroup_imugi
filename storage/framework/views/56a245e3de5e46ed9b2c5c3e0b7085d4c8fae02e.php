				verticalmente como um elevador ou foguete.
			</p>

			<h5>Dolly</h5>

			<p>
				Parecido com o movimento de panorâmica vertical, o dolly é o deslocamento vertical da câmera, 
				executado por equipamentos especiais como tripés hidráulicos de estúdio ou gruas.
				Os movimentos de dolly não servem para deixar evidente a altura de algo, mas são muito bons
				para revelar detalhes de um element vertical. Um bom exemplo seria o dolly de uma exuberante 
				atriz e seu maravilhoso vestido. A proximidade do movimento deixaria em evidência as
				curvas da atriz e os detalhes de seu vestido, ao passo que o deslocamento lento com o campo
				visual fechado revelaria um ar de suspense muito excitante.
			</p>


			<h5>A panorâmica horizontal</h5>

			<p>
				O deslocamento lateral da câmera em torno do próprio eixo permite a ampliação do campo
				visual. Pode parecer, à primeira vista, igual ao travelling, mas possui profundas diferenças.
				Primeiro, a pan-horizontal depende apenas do deslocamento em torno do próprio eixo, enquanto 
				o travelling depende do deslocamento do local através de um equipamento específico.
				A segunda e maior diferença está na dimensão espacial da imagem. Enquanto o travelling expõe 
				uma parte do ambiente que vai se revelado em partes, a panorâmica horizontal permite,
				em um único e breve movimento, revelar toda a dimensão da imagem.
			</p>
			
			<h5>Panorâmica vertical</h5>

			<p>
				A panorâmica vertical é muito utilizada para destacar a altura de algo, geralmente, deixar em

			<h5>Movimentos de camera</h5>

			<p>
				No início da história do cinema, a câmera ficava estática capturando as imagens. Somente as
				pessoas e objetos se moviam diante dela. Cabe lembrar que os equipamentos eram desajeitados 
				e pesados. Em pouco tempo, a câmera passou a captar imagens em movimento de dentro
				de alguns veículos como barcos e trens. surgiam o travelling e os movimentos de aproximação
				e afastamento das cenas. A câmera de cinema ganha então a capacidade de interagir com a

				surpreendente. Nos EUA, durante os anos 10, se popularizou um tipo de cinema que imitava
				vagões de trem e utilizavam filmagens que simulavam viagens pelo país.
			</p>
			<p>
				Hoje o deslocamento lateral da câmera pode ser feito de muitas formas devido ao avanço dos

				afastar a imagem pelo uso do jogo de lentes no momento em que gravavam. A estes efeitos
				foram dados os nomes de zoom-in e zoom-out.
			</p>
			
			<h5>Zoom–in</h5>

			<p>
				É a aproximação da imagem pelo jogo de lentes. Devido à facilidade de execução, este movimento é 
				muito utilizado tanto em publicidade quanto em telejornalismo. Com a capacidade
				de ir de um plano aberto para um plano mais fechado, o zoom-in tem a capacidade de direcionar 
				o olhar do espectador para uma característica específica da imagem, como por exemplo,
				uma pessoa em meio a uma multidão ou uma palavra no meio de um texto.
			</p>


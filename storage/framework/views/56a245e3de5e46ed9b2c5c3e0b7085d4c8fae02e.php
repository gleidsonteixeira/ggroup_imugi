				cinema, as câmeras não possuíam o jogo de lentes objetivas e a distância tinha de ser previamente 
				definida para se determinar qual lente focal seria utilizada. Por isso, uma das grandes
				características dos primeiros filmes foi a utilização do plano geral, grosseiramente utilizado
